int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (*(str + i) != '\0')
	{
		i++;
	}
	return (i);
}

int		ft_strlen_first_line(char *str)
{
	int i;

	i = 0;
	while (*(str + i) != '\n')
	{
		i++;
	}
	return (i);
}
