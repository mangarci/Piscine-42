int		ft_str_is_alpha(char *str)
{
	if (*str == '\0')
	{
		return (1);
	}
	while (((*str >= 'A') && (*str <= 'Z')) || ((*str >= 'a') && (*str <= 'z')))
	{
		str++;
	}
	if (*str == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
